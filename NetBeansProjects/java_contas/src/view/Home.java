    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        User user = new User();
        UserController usercontroller = new UserController();
        user.setId(Integer.parseInt(txtId.getText()));
        user.setUtilizador(txtUtilizador.getText());
        user.setAcesso(txtAcesso.getText());
        user.setEstado(txtEstado.getText());
        String response = usercontroller.update(user);
        if("yes".equals(response)){
            this.getListaUtilizadores();
            this.clearFields();
            JOptionPane.showMessageDialog(null, "Actualiza√ß√£o feita com sucesso");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

