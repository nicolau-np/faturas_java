    private void getListaUtilizadores() {
        DefaultTableModel modelo = (DefaultTableModel) jTUtilizadores.getModel();
        modelo.setNumRows(0);
        UserController usercontroller = new UserController();
        for (User user : usercontroller.list()) {
            modelo.addRow(new Object[]{
                user.getId(),
                user.getUtilizador(),
                user.getAcesso(),
                user.getEstado()
            });
        }
    }

    private void setPermissoes() {
        if ("Administrador".equals(Login.acesso)) {
            jPanel1.setVisible(true);
        } else if ("Utilizador Normal".equals(Login.acesso)) {
            jPanel1.setVisible(false);
        }
    }

    private void setPerfil() {
        lblUtilizador.setText(Login.utilizador);
        lblAcesso.setText(Login.acesso);
    }

    private void clearFields() {
       txtUtilizador.setText(null);
       txtAcesso.setText(null);
       txtId.setText(null);
       txtEstado.setText(null);
    }

